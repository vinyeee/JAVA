package day01;

public class LiteralTest1 {
	public static void main(String[] args) {		
		System.out.println(10);         // int
		System.out.println(10.0);       // double
		System.out.println(10.0f);      // float
		System.out.println(10L);        // long (소문자도 가능)
		System.out.println(true);       // boolean
		System.out.println(false);      // boolean
		System.out.println('A');        // char
		System.out.println("A");        // String
		System.out.println('가');   		// char
		System.out.println("가");        // String
		System.out.println("ABC");      // String
		System.out.println("가나다");		// String
	}
}
